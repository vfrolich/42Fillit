#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int is1;
	int is2;

	is2 = 0;
	is1 = ft_strlen(s1);
	while (s2[is2] != '\0')
	{
		s1[is1] = s2[is2];
		is1++;
		is2++;
	}
	s1[is1] = '\0';
	return (s1);
}
